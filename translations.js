// Función para cambiar idioma MEJORADA
function changeLanguage(lang) {
  console.log('Cambiando idioma a:', lang);
  
  // Guardar preferencia
  localStorage.setItem('preferredLanguage', lang);
  
  // Actualizar botones de idioma
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.remove('active');
  });
  const activeBtn = document.querySelector(`[data-lang="${lang}"]`);
  if (activeBtn) activeBtn.classList.add('active');
  
  // Traducir elementos con data-translate
  const elements = document.querySelectorAll('[data-translate]');
  console.log('Elementos a traducir:', elements.length);
  
  elements.forEach(element => {
    const key = element.getAttribute('data-translate');
    const keys = key.split('.');
    let translation = translations[lang];
    
    // Navegar por el objeto de traducciones
    for (let k of keys) {
      if (translation && translation[k] !== undefined) {
        translation = translation[k];
      } else {
        console.warn(`Traducción no encontrada: ${key} para idioma ${lang}`);
        translation = null;
        break;
      }
    }
    
    if (translation) {
      element.textContent = translation;
    }
  });
  
  // Traducir placeholders
  const placeholders = document.querySelectorAll('[data-translate-placeholder]');
  placeholders.forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    const keys = key.split('.');
    let translation = translations[lang];
    
    for (let k of keys) {
      if (translation && translation[k] !== undefined) {
        translation = translation[k];
      } else {
        translation = null;
        break;
      }
    }
    
    if (translation) {
      element.placeholder = translation;
    }
  });
  
  // Cambiar dirección del texto para árabe
  if (lang === 'ar') {
    document.documentElement.dir = 'rtl';
    document.documentElement.lang = 'ar';
  } else {
    document.documentElement.dir = 'ltr';
    document.documentElement.lang = lang;
  }
  
  // Actualizar el título de la página
  const titleTranslations = {
    'es': 'Archipiélago Digital - Mostapha El Jaidi | Biólogo Informático',
    'en': 'Digital Archipelago - Mostapha El Jaidi | Biologist Programmer', 
    'fr': 'Archipel Numérique - Mostapha El Jaidi | Biologiste Informaticien',
    'ar': 'الأرخبيل الرقمي - مصطفى الجعيدي | عالم أحياء مبرمج'
  };
  document.title = titleTranslations[lang] || titleTranslations.es;
}

// Cargar idioma guardado al iniciar
document.addEventListener('DOMContentLoaded', function() {
  const savedLang = localStorage.getItem('preferredLanguage') || 'es';
  
  // Pequeño delay para asegurar que el DOM esté completamente cargado
  setTimeout(() => {
    changeLanguage(savedLang);
  }, 100);
  
  // Event listeners para botones de idioma
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', function() {
      const lang = this.getAttribute('data-lang');
      changeLanguage(lang);
    });
  });
});